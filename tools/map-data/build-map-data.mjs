import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const here = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(here, "../..");
const sourcePath = path.join(here, "source-markers.json");
const outputPath = path.join(root, "assets/data/map.js");
const source = JSON.parse(fs.readFileSync(sourcePath, "utf8"));
const bounds = source.bounds;

const round = (value) => Number(value.toFixed(8));
const normalize = (mapX, mapY) => ({
  x: round((mapX - bounds.minX) / (bounds.maxX - bounds.minX)),
  y: round((bounds.maxY - mapY) / (bounds.maxY - bounds.minY))
});

const coordinateModel = {
  id: "palpagos-map-v1",
  units: "in-game map coordinates",
  origin: "in-game map grid",
  xAxis: "east-positive",
  yAxis: "north-positive",
  screenNormalized: {
    x: "(mapX - minX) / (maxX - minX)",
    y: "(maxY - mapY) / (maxY - minY)",
    range: [0, 1],
    yAxis: "down-positive"
  },
  gameDataToMap: {
    mapX: "(dataY - 158000) / 459",
    mapY: "(dataX + 123888) / 459",
    note: "The game-data X/Y axes are flipped when converted to displayed map coordinates."
  }
};

const markers = source.markers.map((marker) => {
  const normalized = normalize(marker.mapX, marker.mapY);
  return {
    id: marker.id,
    mapId: marker.mapId,
    category: marker.category,
    name: marker.name,
    x: normalized.x,
    y: normalized.y,
    description: marker.description || "",
    level: marker.level ?? null,
    pal: marker.pal ?? null,
    item: marker.item ?? null,
    region: marker.region ?? null,
    sourceIds: marker.sourceIds,
    coordinates: { mapX: marker.mapX, mapY: marker.mapY },
    traits: marker.traits || []
  };
});

const runtime = {
  meta: {
    schemaVersion: "palbreed.map.v1",
    dataVersion: source.dataVersion,
    gameVersion: source.gameVersion,
    generatedAt: source.generatedAt,
    markerCount: markers.length,
    categoryCount: source.categories.length,
    bounds,
    coordinateModel,
    license: {
      data: "CC BY-SA 4.0",
      dataUrl: "https://creativecommons.org/licenses/by-sa/4.0/",
      attribution: "Location data adapted from The Palworld Wiki contributors (palworld.wiki.gg), CC BY-SA 4.0.",
      basemap: "The primary basemap is a locally optimized derivative of World Map.webp, a copyrighted Palworld game image owned by Pocketpair and used for identification and orientation under the source page's fair-use notice. The original PalBreed schematic remains as the offline fallback.",
      basemapSourceUrl: "https://palworld.wiki.gg/wiki/File:World_Map.webp",
      palworldNotice: "Palworld and related names are trademarks or copyrighted works of their respective owners. This is an unofficial community dataset."
    },
    sources: source.sources,
    limitations: [
      "The primary basemap is the coordinate-aligned in-game world texture; the original schematic remains available as a load-failure fallback.",
      "The basemap snapshot predates some Palworld 1.0-only regions, so newer markers can appear over ocean where updated official texture coverage is not yet available.",
      "The MVP prioritizes towers, watchtowers, sealed realms, fixed overworld Alpha Pals, selected fast-travel points, oil rigs, and verified Skill Fruit Trees.",
      "Random dungeon entrances, collectibles, merchants, fishing spots, Pal habitats, and exhaustive raw resource-node clusters are not included yet.",
      "Palworld 1.0 introduced additional regions and content; markers should be refreshed when the cited source tables change."
    ]
  },
  maps: source.maps.map((map) => ({ ...map, bounds, coordinateModel })),
  categories: source.categories,
  markers
};

const banner = `/*\n * PalBreed offline map data — ${runtime.meta.dataVersion}\n * Data: CC BY-SA 4.0; attribution and source revisions are in PALBREED_MAP_DATA.meta.\n * Generated by tools/map-data/build-map-data.mjs. Do not hand-edit.\n */\n`;
const js = `${banner}(function (root) {\n  \"use strict\";\n  root.PALBREED_MAP_DATA = ${JSON.stringify(runtime, null, 2)};\n})(typeof window !== \"undefined\" ? window : globalThis);\n`;

fs.writeFileSync(outputPath, js, "utf8");
console.log(`Wrote ${path.relative(root, outputPath)} with ${markers.length} markers.`);
